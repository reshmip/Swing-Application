    public void populateOrgNameComboBox()
    {
        orgNameComboBox.removeAllItems();
        
        for (Organization organization : orgDir.getOrganizationList()){
            orgNameComboBox.addItem(organization);
        }
    }
    
    public void populateOrgTypeComboBox()
    {
        orgTypeComboBox.removeAllItems();
        
        for (Organization organization : orgDir.getOrganizationList()){
            orgTypeComboBox.addItem(organization);
        }
    }
    
    public void populateTable(Organization organization)
    {
        DefaultTableModel model = (DefaultTableModel) employeeTable.getModel();
        
        model.setRowCount(0);
        
        for (Person person : organization.getPersonDir().getPersonDir()){
            Object[] row = new Object[2];
            row[0] = person.getPersonId();
            row[1] = person.getPersonName();
            model.addRow(row);
        }
    }
