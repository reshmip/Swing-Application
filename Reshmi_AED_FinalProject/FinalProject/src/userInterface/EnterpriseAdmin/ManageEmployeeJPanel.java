    private void addEmpBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addEmpBtnActionPerformed
        Organization organization = (Organization) orgTypeComboBox.getSelectedItem();
        String name = empNameTxt.getText();
        if(name.equals("")){
            JOptionPane.showMessageDialog(null, "Enter the employee name", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        else if((organization.getPersonDir().getPersonDir().size()==1)&& 
                (organization instanceof HospitalAdminOrg || organization instanceof EnergyResourceOrg)){
            JOptionPane.showMessageDialog(null, "Admin already created for this organization", "WARNING", JOptionPane.WARNING_MESSAGE);
        }
        else{
        organization.getPersonDir().addPerson(name);
        JOptionPane.showMessageDialog(null, "New employee added!", "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
        }
        empNameTxt.setText("");
        
    }//GEN-LAST:event_addEmpBtnActionPerformed


