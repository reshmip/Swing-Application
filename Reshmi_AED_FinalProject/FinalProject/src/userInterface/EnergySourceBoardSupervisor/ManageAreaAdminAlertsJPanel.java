    public void populateAreatoEnergyAdminAlerts(){
        DefaultTableModel dtm = (DefaultTableModel)areaAdminAlertTable.getModel();
        dtm.setRowCount(0);
        if(userAccount.getWorkQueue().getWorkRequestList()!=null){
        for (WorkRequest request : userAccount.getWorkQueue().getWorkRequestList())
        {
            if(request instanceof AreaAdmintoEnergyAdminRequest){
            if(userAccount.getWorkQueue().getWorkRequestList()!=null)
            {
            Object[] row = new Object[6];
            
            row[0] = request;
            row[1] = request.getSender();
            row[2] = request.getRequestDate();
            row[3] = ((AreaAdmintoEnergyAdminRequest)request).getComment();
            row[4] = request.getStatus();
            row[5] = ((AreaAdmintoEnergyAdminRequest)request).getResponse();
            dtm.addRow(row);
            }
            }
        }
        }
    }
