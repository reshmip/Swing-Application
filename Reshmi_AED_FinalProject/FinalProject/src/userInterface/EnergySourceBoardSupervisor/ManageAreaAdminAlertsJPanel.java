    private void sendBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendBtnActionPerformed
        int selectedRow = areaAdminAlertTable.getSelectedRow();
        if(selectedRow>=0){
            
            AreaAdmintoEnergyAdminRequest request = (AreaAdmintoEnergyAdminRequest)areaAdminAlertTable.getValueAt(selectedRow, 0);
            if(request.getStatus().equals("sent")){
                if(!responseTxt.getText().trim().isEmpty()){
            String response = responseTxt.getText();
            request.setResponse(response);
            request.setStatus("checked");
            populateAreatoEnergyAdminAlerts();
            }else{
                    JOptionPane.showMessageDialog(null, "Kindly enter a response message", "ERROR", JOptionPane.ERROR_MESSAGE);
                }
            }
                else{
                JOptionPane.showMessageDialog(null, "Response message is already sent", "MESSAGE", JOptionPane.INFORMATION_MESSAGE);
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select a row", "WARNING", JOptionPane.WARNING_MESSAGE);
            return; 
        }
    }//GEN-LAST:event_sendBtnActionPerformed

