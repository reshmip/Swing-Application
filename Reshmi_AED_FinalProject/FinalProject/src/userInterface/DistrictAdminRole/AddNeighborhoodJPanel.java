    public void populateNeighborhoodTable()
    {
        DefaultTableModel model = (DefaultTableModel) neighborhoodAreasTable.getModel();

        model.setRowCount(0);
        for (Neighborhood neighborhood : ecoDistrict.getNeighborhoodList()) {
            Object[] row = new Object[1];
            row[0] = neighborhood.getNeighborhoodName();
            model.addRow(row);
        }
    }
