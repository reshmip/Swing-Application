    private void addAreasBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addAreasBtnActionPerformed
        // TODO add your handling code here:
        String areaName = areaNameTxt.getText();
        for(Neighborhood neighborhood: ecoDistrict.getNeighborhoodList()){
        if(neighborhood.getNeighborhoodName().equalsIgnoreCase(areaName)){
          JOptionPane.showMessageDialog(null, "Neighborhood already exists","Warning",JOptionPane.WARNING_MESSAGE);
         
            return;   
        }}
         if(areaName == null || areaName.trim().equals("")){
             JOptionPane.showMessageDialog(null, "Kindly enter the area name!","Warning",JOptionPane.WARNING_MESSAGE);
         
            return;
        }
         else{
              Neighborhood neighborhood = ecoDistrict.createAddNeighborhoods();
            neighborhood.setNeighborhoodName(areaName);
            populateNeighborhoodTable();  
            JOptionPane.showMessageDialog(null, "Neighborhood is added!", "New Area", JOptionPane.INFORMATION_MESSAGE);
       
         }
        areaNameTxt.setText("");
    }//GEN-LAST:event_addAreasBtnActionPerformed


